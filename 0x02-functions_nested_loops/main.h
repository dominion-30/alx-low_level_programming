
/**
* _putchar - writes the character c to stdout
* @c: The character to print
*
* Return: On success 1.
* On error, -1 is returned, and errno is set appropriately.
*/
int _putchar(char c);


	

/**
* print_alphabet - write the letters to stdout
*
* Return: On success 1.
*/

void print_alphabet(void);


/**
* print 10x the alphabet in lowercase followed by newline
*
* Return: void
*/

void print_alphabet_x10(void);

/**
* int _islower(int c) checks for lowercase characters
*
* Return 1 if c is lower
* Return 0 otherwise
*/

int _islower(int c);


/**
* int _isalpha(int c) checks for alphabetic characters
* Return 1 is c is a letter lower or upper case
* Return 0 otherwise
*/
int _isalpha(int c);
